def test_parse_exclude_newer_equals_form(tmp_path: Path) -> None:
    p = tmp_path / "pylock.toml"
    p.write_text(
        "# This file was autogenerated by uv via the following command:\n"
        "#    uv pip compile pyproject.toml --exclude-newer=2025-06-01T12:30:45Z --quiet\n"
        'lock-version = "1.0"\n',
        encoding="utf-8",
    )
    assert pg.parse_exclude_newer_from_lockfile(p) == "2025-06-01T12:30:45Z"


