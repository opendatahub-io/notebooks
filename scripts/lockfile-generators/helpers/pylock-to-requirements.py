#!/usr/bin/env python3
"""Convert a pylock.toml (PEP 751) to a pip-compatible requirements.txt.

This helper is called by create-requirements-lockfile.sh (Step 2) to transform
a uv-generated pylock.toml lock file into a requirements.txt that pip, uv, and
cachi2 can consume.

Why not just use pylock.toml directly?
  - pip and cachi2 don't support pylock.toml (PEP 751) yet.
  - requirements.txt with --hash lines is the common format understood by
    pip install --require-hashes, uv pip install --verify-hashes, and cachi2
    pip prefetch.

Usage:
    python3 pylock-to-requirements.py <pylock.toml> <requirements.txt> [index-url]

Arguments:
    pylock.toml       Input lock file (PEP 751 format, generated by uv pip compile)
    requirements.txt  Output file path
    index-url         (optional) PyPI index URL to emit as --index-url header line.
                      If omitted or empty, the URL is taken from the uv compile comment
                      in the pylock (--default-index=...) so the requirements file matches
                      the index the lock was resolved against.

Output format:
    Each package entry becomes a line like:
        name==version ; marker \\
            --hash=sha256:abc123 \\
            --hash=sha256:def456

    Pure-Python packages (py3-none-any.whl) have a single hash.
    Native/binary packages have one hash per architecture wheel.

    Packages locked as a direct URL (uv ``archive``) become ``name @ URL`` so
    pip/Hermeto do not look them up on the index (e.g. tensorflow-rocm from AMD).

    If index-url is provided, it is emitted as the first line:
        --index-url https://...
"""
import re
import sys
import tomllib
from pathlib import Path

# uv pip compile records its index in the first-line comment, e.g.
#   --default-index=https://pypi.org/simple
_DEFAULT_INDEX_RE = re.compile(r"--default-index=(https?://\S+)")


def extract_default_index_from_pylock(pylock_path: Path) -> str:
    """Return --default-index URL from the uv autogeneration comment, or ""."""
    try:
        head = pylock_path.read_text(encoding="utf-8", errors="replace")[:8192]
    except OSError:
        return ""
    m = _DEFAULT_INDEX_RE.search(head)
    return m.group(1).rstrip("'\"") if m else ""


def main():
    if len(sys.argv) < 3:
        print(f"Usage: {sys.argv[0]} <pylock.toml> <requirements.txt> [index-url]",
              file=sys.stderr)
        sys.exit(1)

    pylock_path = Path(sys.argv[1])
    output_path = Path(sys.argv[2])
    if len(sys.argv) > 3 and sys.argv[3].strip():
        index_url = sys.argv[3].strip()
    else:
        index_url = extract_default_index_from_pylock(pylock_path)

    # Parse the pylock.toml (PEP 751 format, requires Python 3.11+ for tomllib)
    with open(pylock_path, "rb") as f:
        data = tomllib.load(f)

    lines = []

    # Emit --index-url as the first line so pip/uv know where to find packages
    if index_url:
        lines.append(f"--index-url {index_url}")

    # Process each [[packages]] entry from the lock file
    for pkg in data.get("packages", []):
        name = pkg["name"]
        version = pkg["version"]
        marker = pkg.get("marker", "")  # e.g. "sys_platform == 'linux'"

        archive = pkg.get("archive")
        if isinstance(archive, dict) and (archive_url := (archive.get("url") or "").strip()):
            # Direct URL (not on PyPI): PEP 508 "name @ URL" so prefetchers fetch this file.
            hashes = [
                f"--hash={algo}:{digest}"
                for algo, digest in archive.get("hashes", {}).items()
            ]
            entry = f"{name} @ {archive_url}"
            if marker:
                entry += f" ; {marker}"
            if hashes:
                entry += " \\\n" + " \\\n".join(f"    {h}" for h in hashes)
            lines.append(entry)
            continue

        # Collect hashes from wheels and sdist (index-served packages).
        hashes = []
        for whl in pkg.get("wheels", []):
            for algo, digest in whl.get("hashes", {}).items():
                hashes.append(f"--hash={algo}:{digest}")
        sdist = pkg.get("sdist")
        if isinstance(sdist, dict):
            for algo, digest in sdist.get("hashes", {}).items():
                hashes.append(f"--hash={algo}:{digest}")

        entry = f"{name}=={version}"
        if marker:
            entry += f" ; {marker}"
        if hashes:
            entry += " \\\n" + " \\\n".join(f"    {h}" for h in hashes)

        lines.append(entry)

    # Write the output file
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text("\n".join(lines) + "\n")

    pkg_count = len(lines) - (1 if index_url else 0)
    print(f"  Generated {output_path} ({pkg_count} packages)")


if __name__ == "__main__":
    main()
